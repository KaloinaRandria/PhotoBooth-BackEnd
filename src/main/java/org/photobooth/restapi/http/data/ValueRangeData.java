package org.photobooth.restapi.http.data;

public class ValueRangeData {
    private int id;
    private double price;

    public ValueRangeData() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}

